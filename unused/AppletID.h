#ifndef _APPLETID_H_
#define _APPLETID_H_ (1)

#define kAppletID_UserMin (0x7170) /**< Smallest permitted unregistered ID. */
#define kAppletID_UserMax (0x717f) /**< Larges permitted unregistered ID. */

#define kAppletID_GroupMin (0x7100) /**< Smallest permitted Flickr group ID.   \
                                     */
#define kAppletID_GroupMax (0x717f) /**< Largest permitted Flickr group ID. */

#define kAppletID_ASMin (0xa000) /**< Smallest AS/RL ID. */
#define kAppletID_ASMax (0xafff) /**< Largest AS/RL ID. */

#endif // _APPLETID_H_
